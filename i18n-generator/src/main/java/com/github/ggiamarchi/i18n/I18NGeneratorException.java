package com.github.ggiamarchi.i18n;

public class I18NGeneratorException extends RuntimeException {

	private static final long serialVersionUID = 3889520282869349654L;

	public I18NGeneratorException(String message) {
		super(message);
	}

	public I18NGeneratorException(String message, Throwable cause) {
		super(message, cause);
	}

	public I18NGeneratorException(Throwable cause) {
		super(cause);
	}

}
